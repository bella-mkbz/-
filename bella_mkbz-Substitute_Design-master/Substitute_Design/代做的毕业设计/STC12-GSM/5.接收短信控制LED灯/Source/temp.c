/*******************************************************************************
* 函数名         : Delay1ms
* 函数功能		   : 延时函数
* 输入           : 无
* 输出         	 : 无
*******************************************************************************/

void Delay642us()		//@11.0592MHz
{
	unsigned char i, j;

	_nop_();
	_nop_();
	i = 7;
	j = 228;
	do
	{
		while (--j);
	} while (--i);
}

